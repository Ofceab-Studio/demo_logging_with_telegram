  /// Add bold text
  void addBoldText(String text);
}

abstract class HasNormalStyle {
  /// Add Normal text
  void addNormalText(String text);
}

abstract class HasItalicStyle {
  /// Add italic text
  void addItalicText(String text);
}

abstract class HasUnderLineStyle {
  /// Add Underline text
  void addUnderlineText(String text);
}

abstract class HasCodeStyle {
  /// Add Code text
  void addCodeText(String text);
}

abstract class HasMentionStyle {
  /// Mention some one
  void addMention(String user);
}
